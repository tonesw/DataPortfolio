/* TSQL Query utilizing @COUNTER variable as a Boolean value to verify if specified
	customer code exists and returning name if so									*/
DECLARE @MYCUSCODE INT, @MYFNAME VARCHAR(20),@MYLNAME VARCHAR(20), @COUNTER INT;
SET @MYCUSCODE = 10010;
BEGIN
SET @COUNTER = (SELECT COUNT(CUS_CODE)
FROM CUSTOMER
WHERE CUS_CODE = @MYCUSCODE)

IF @COUNTER = 1
	BEGIN
		SET @MYFNAME = (SELECT CUS_FNAME FROM CUSTOMER WHERE CUS_CODE = @MYCUSCODE);
		SET @MYLNAME = (SELECT CUS_LNAME FROM CUSTOMER WHERE CUS_CODE = @MYCUSCODE);
		PRINT CONCAT(@MYFNAME, ' ', @MYLNAME);
	END;
ELSE IF @COUNTER = 0
	BEGIN
		PRINT 'Customer does not exist.';
	END;
END;